double termistor(int adcValue) {
	double temperature;
	temperature = log(((40950000 / adcValue) - 10000));
	temperature = 1
			/ (0.001179148
					+ (0.000234125
							+ (0.0000000876741 * temperature * temperature))
							* temperature);
	temperature = temperature - 273.15;
	return temperature;
}
